            'last_name' => fake()->lastName(),
            'email' => fake()->unique()->safeEmail(),
            'phone' => fake()->phoneNumber(),
            'job_title' => fake()->jobTitle(),
            'notes' => fake()->optional()->sentence(),
            'status' => fake()->randomElement(['active', 'inactive', 'lead']),
